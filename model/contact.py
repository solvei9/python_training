class Contact:
    def __init__(self, firstname, lastname, nickname, title, company, address, mobile, email, bday, bmonth,
                      byear):
        self.firstname = firstname
        self.lastname = lastname
        self.nickname = nickname
        self.title = title
        self.company = company
        self.address = address
        self.mobile = mobile
        self.email = email
        self.bday = bday
        self.bmonth = bmonth
        self.byear = byear
