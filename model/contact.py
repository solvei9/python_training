class Contact:
    def __init__(self, firstname=None, lastname=None, nickname=None, title=None, company=None, address=None, mobile=None,
                 email=None, bday=None, bmonth=None, byear=None, contact_id=None):
        self.firstname = firstname
        self.lastname = lastname
        self.nickname = nickname
        self.title = title
        self.company = company
        self.address = address
        self.mobile = mobile
        self.email = email
        self.bday = bday
        self.bmonth = bmonth
        self.byear = byear
        self.contact_id = contact_id
